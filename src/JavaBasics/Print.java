package JavaBasics;

public class Print {
    public static void main(String []args){
        System.out.println("Hello World");
        System.out.print("Hello World\n");
        //Print Triangle
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        //in a line
        System.out.println("*\n**\n***\n****");
        //Print Z
        System.out.println("*****");
        System.out.println("   *");
        System.out.println("  *");
        System.out.println(" *");
        System.out.println("*****");
    }
}
